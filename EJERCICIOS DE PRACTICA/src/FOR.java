public class FOR {

    public static void main(String[] args) {
        {
            //Incremento i++
            for (int i = 1; i <= 10; i++) {
                System.out.println(("i++ = ")+i);
            }

            System.out.println("");

            //Decremento i--
            for (int i = 10; i > 0; i--) {
                System.out.println(("i-- = ")+i);
            }
        }
    }
}
